
public class calculator {
	
	public double add(double x, double y){
		
		double resultat = x + y;
		return resultat;
	}
	
	public double sub (double x, double y){
		double resultat = x - y;
		return resultat;
	}
	public double mul(double x, double y){
		double resultat = x * y;
		return resultat;
	}
	public double div(double x, double y){
		double resultat = x / y;
		return resultat;
	}
	

}
